package model;

public enum Tipo {
	
	STANDARD , SUPERIOR, DELUXE, SUITE, STUDIO 
}

//STANDARD (Padrão): O tipo de quarto mais básico e comum.

//SUPERIOR: Um pouco maior ou com uma vista melhor que o Standard.

//DELUXE (Luxo): Mais espaçoso, com mobília de alta qualidade e mais comodidades.

//SUITE: Possui áreas separadas, como uma sala de estar e um quarto.

//SUITE_JUNIOR: Uma versão menor de uma suíte, às vezes sem uma parede completa dividindo os ambientes.

//SUITE_PRESIDENCIAL: A suíte mais luxuosa e cara do hotel.

//STUDIO: Um quarto com um pequeno espaço de cozinha (kitchenette).
